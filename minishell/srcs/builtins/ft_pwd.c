void	ft_pwd(void)
{
	int	i;

	i = 0;
	while (g_mini_env[i])
	{
		if (g_mini_env[i][0] == 'P' && g_mini_env[i][1] == 'W'
		&& g_mini_env[i][2] == 'D')
		{
			printf("%s\n", g_mini_env[i] + 5);
		}
		i++;
	}
}