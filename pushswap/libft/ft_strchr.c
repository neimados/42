#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	c = (char)c;
	while (*str)
	{
		if (*str == c)
			return ((void *) str);
		str++;
	}
	if (c == '\0')
		return ((void *) str);
	return (NULL);
}
